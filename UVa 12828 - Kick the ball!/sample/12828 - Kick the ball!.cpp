#include <stdio.h>

int main() {
	double A[5], B[5];
	char s[5];
	int cases = 0, ga, gb;
	while (scanf("%lf", &A[0]) == 1) {
		for (int i = 1; i < 5; i++)
			scanf("%lf", &A[i]);
		for (int i = 0; i < 5; i++)
			scanf("%lf", &B[i]);
		scanf("%s", s);
		ga = s[0] - '0', gb = s[2] - '0';
		double dp[16][16][16] = {};
		double result[16][16] = {};
		dp[0][0][0] = 1;
		int a = 0, b = 0;
		for (int i = 0; i <= 10; i++) {
			for (int p = 0; p <= 5; p++) {
				for (int q = 0; q <= 5; q++) {
					if ((p > q && q + (5 - b) < p) || i == 10) {
						result[p][q] += dp[i][p][q];
						dp[i][p][q] = 0;
					}
					if ((q > p && p + (5 - a) < q) || i == 10) {
						result[p][q] += dp[i][p][q];
						dp[i][p][q] = 0;
					}
				}
			}
			if (i&1) {
				if (b < 5)	
				for (int p = 0; p <= 5; p++) {
					for (int q = 0; q <= 5; q++) {
						dp[i+1][p][q] += dp[i][p][q] * (1 - B[b]);
						dp[i+1][p][q+1] += dp[i][p][q] * B[b];
					}
				}
				b++;
			} else {
				if (a < 5)
				for (int p = 0; p <= 5; p++) {
					for (int q = 0; q <= 5; q++) {
						dp[i+1][p][q] += dp[i][p][q] * (1 - A[a]);
						dp[i+1][p+1][q] += dp[i][p][q] * A[a];
					}
				}
				a++;
			}
		}
		printf("Case %d: %.2lf%%\n", ++cases, result[ga][gb] * 100.0);
	}
	return 0;
}

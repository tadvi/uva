vector<int> dsum[50];
vector<int> msum[50][3][100];
char Num[100000][6];
char p[100000] = {}, head[50][100000] = {}, lsum[50][10][50] = {};
void process(int n) {
    static char s[9];
    int i, j, k, l;
    sprintf(s, "%d", n);
    sprintf(Num[n], "%d", n);
    for(i = 0, j = 0; s[i]; i++)
        j += s[i]-'0';

    dsum[j].push_back(n);

    k = (s[0]-'0')*10+s[4]-'0';
    msum[j][0][k].push_back(n);

    k = (s[1]-'0')*10+s[3]-'0';
    msum[j][1][k].push_back(n);

    k = s[2]-'0';
    msum[j][2][k].push_back(n);
    k = 0, l = 0;
    for(i = 0; i < 5; i++) {
        k = k*10+s[i]-'0';
        l += s[i]-'0';
        head[j][k] = 1;
        lsum[j][i][l] = 1;
    }
}
void sieve() {
    int i, j;
    int cnt = 0;
    for(i = 2; i < 100000; i++) {
        if(p[i] == 0) {
            for(j = i+i; j < 100000; j += i)
                p[j] = 1;
            if(i > 10000) {
                cnt++;
                process(i);
            }
        }
    }
}
int g[50][50], mm, d;
int ng[50][50], rown[6], rowl[6];
char buf[50];
set<string> Ans;
void dfs(int idx, int t10) {
    int k, i, j, l;
    for(i = 0; i < 5; i++) {
        k = rown[i], l = rowl[i];
        if(l > mm)
            return ;
        if(idx && lsum[mm][idx-1][l] == 0)
            return ;
        if(head[mm][k] == 0 && idx)
            return ;
        if(idx == 5 && l != mm)
            return ;
    }
    if(idx == 5) {
        l = 0;
        for(i = 0; i < 5; i++)
            for(j = 0; j < 5; j++)
                buf[l++] = ng[i][j]+'0';
        buf[l] = '\0';
        Ans.insert(buf);
        return ;
    }
    if(idx == 0 || idx == 4) {
        k = g[idx][0]*10 + g[idx][4];
        j = 0;
    } else if(idx == 1 || idx == 3) {
        k = g[idx][1]*10 + g[idx][3];
        j = 1;
    } else {
        k = g[idx][2];
        j = 2;
    }
    for(i = 0; i < msum[mm][j][k].size(); i++) {
        for(l = 0; l < 5; l++) {
            ng[idx][l] = Num[msum[mm][j][k][i]][l]-'0';
            rown[l] = rown[l]*10 + ng[idx][l];
            rowl[l] = rowl[l] + ng[idx][l];
        }
        dfs(idx+1, t10*10);
        for(l = 0; l < 5; l++) {
            ng[idx][l] = Num[msum[mm][j][k][i]][l]-'0';
            rown[l] = rown[l]/10;
            rowl[l] = rowl[l] - ng[idx][l];
        }
    }
}
void sol(int mm, int d) {
    int x, y;
    int i, j, k;
    for(i = 0; i < dsum[mm].size(); i++) {
        for(j = 0; j < dsum[mm].size(); j++) {
            x = dsum[mm][i], y = dsum[mm][j];
            if(Num[x][2] == Num[y][2] && Num[x][0] == d+'0') {
                for(k = 0; k < 5; k++) {
                    g[k][k] = Num[x][k]-'0';
                    g[4-k][k] = Num[y][k]-'0';
                }
                dfs(0, 1);
            }
        }
    }
}
